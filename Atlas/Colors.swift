import UIKit

extension UIColor {

    static func atlasBlue() -> UIColor {
        return UIColor(red: 0.09, green: 0.56, blue: 0.83, alpha: 1.0)
    }

    static func atlasGray() -> UIColor {
        return UIColor(red: 0.60, green: 0.60, blue: 0.60, alpha: 1.0)
    }

}
